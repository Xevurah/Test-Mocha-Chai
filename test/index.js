
describe('Sum',()=>{
    // Suma de + con + tiene que ser +
    it ('Sum of + with + have to be +',()=>{
        console.log(2 + 2);
        console.log(4);
    });
    // Suma de + y mayor que el segundo elemento que es - = +
    it ('Sum of + and greater than second element that is - = +',()=>{
        console.log(3 + (-2));
        console.log(1);
    });
});